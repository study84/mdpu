def main():
    print("=" * 60)
    print("     ХЕШУВАННЯ ПАРОЛЯ (SHA-256) — Лабораторна робота")
    print("=" * 60)

    password = input("\nВведіть пароль : ").strip()
    if not password:
        print("Помилка: пароль не може бути порожнім!")
        return

    # ── Хешування ──
    print("\n[ Хешування пароля ]")
    salt, hashed = hash_password(password)

    print(f"  Пароль          : {password}")
    print(f"  Сіль (salt)     : {salt}")
    print(f"  Солений пароль  : {salt + password}")
    print(f"  SHA-256 хеш     : {hashed}")

    # ── Перевірка правильного пароля ──
    print("\n[ Перевірка пароля ]")
    test_correct = input("  Введіть пароль для перевірки : ").strip()
    if verify_password(test_correct, salt, hashed):
        print("  ✓ Пароль правильний!")
    else:
        print("  ✗ Пароль невірний!")

    # ── Демонстрація лавинного ефекту ──
    print("\n[ Лавинний ефект ]")
    pw2 = password[:-1] + chr(ord(password[-1]) + 1) if password else 'x'
    _, hash2 = hash_password(pw2, salt)  # та сама сіль, інший пароль

    diff = sum(1 for a, b in zip(
        bin(int(hashed, 16))[2:].zfill(256),
        bin(int(hash2,  16))[2:].zfill(256)
    ) if a != b)

    print(f"  Пароль 1 : '{password}' → {hashed}")
    print(f"  Пароль 2 : '{pw2}'  → {hash2}")
    print(f"  Різних біт у хеші: {diff} з 256 ({diff/256*100:.1f}%)")

    # ── Демонстрація захисту від rainbow-таблиць ──
    print("\n[ Захист сіллю (два хеші одного пароля з різними солями) ]")
    salt_a, hash_a = hash_password(password)
    salt_b, hash_b = hash_password(password)
    print(f"  Сіль A : {salt_a}  →  {hash_a}")
    print(f"  Сіль B : {salt_b}  →  {hash_b}")
    print(f"  Хеші однакові? {'Так' if hash_a == hash_b else 'Ні — сіль захищає!'}")

    print("=" * 60)


if __name__ == "__main__":
    main()
