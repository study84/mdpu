H_INIT = [
    0x6a09e667, 0xbb67ae85, 0x3c6ef372, 0xa54ff53a,
    0x510e527f, 0x9b05688c, 0x1f83d9ab, 0x5be0cd19,
]

MASK32 = 0xFFFFFFFF


# ──────────────────────────────────────────────────────────
# Бітові операції
# ──────────────────────────────────────────────────────────

def ror32(x, n):
    """Циклічний зсув 32-бітного числа вправо на n біт."""
    return ((x >> n) | (x << (32 - n))) & MASK32

def shr32(x, n):
    """Логічний зсув вправо на n біт."""
    return (x >> n) & MASK32


# ──────────────────────────────────────────────────────────
# Padding (доповнення повідомлення)
#
# SHA-256 обробляє дані блоками по 512 біт (64 байти).
# Повідомлення доповнюється так:
#   1. Додається біт '1'
#   2. Додаються нулі до довжини ≡ 448 (mod 512)
#   3. Додається довжина оригінального повідомлення у бітах (64 біти)
# ──────────────────────────────────────────────────────────

def pad_message(message: bytes) -> bytes:
    """Доповнення повідомлення за правилами SHA-256."""
    length = len(message) * 8          # довжина у бітах
    message += b'\x80'                 # додаємо біт '1' (0x80 = 10000000)

    # Доповнюємо нулями до 56 байт у межах блоку (512 - 64 = 448 біт)
    while len(message) % 64 != 56:
        message += b'\x00'

    # Додаємо довжину оригінального повідомлення (8 байт, big-endian)
    message += struct.pack('>Q', length)
    return message


# ──────────────────────────────────────────────────────────
# Основний алгоритм SHA-256
# ──────────────────────────────────────────────────────────

def sha256(message: str) -> str:
    """
    Обчислює SHA-256 хеш рядка.
    Повертає hex-рядок з 64 символів (256 біт).
    """
    # Перетворюємо рядок у байти
    data = message.encode('utf-8')

    # Доповнення повідомлення
    data = pad_message(data)

    # Початкові значення хешу (копіюємо, щоб не змінювати оригінал)
    h = H_INIT[:]

    # Обробка блоків по 64 байти (512 біт)
    for i in range(0, len(data), 64):
        block = data[i:i+64]

        # Розбиваємо блок на 16 слів по 32 біти (big-endian)
        w = list(struct.unpack('>16I', block))

        # Розширюємо до 64 слів
        for j in range(16, 64):
            # σ0 та σ1 — функції розширення
            s0 = ror32(w[j-15], 7) ^ ror32(w[j-15], 18) ^ shr32(w[j-15], 3)
            s1 = ror32(w[j-2], 17) ^ ror32(w[j-2],  19) ^ shr32(w[j-2],  10)
            w.append((w[j-16] + s0 + w[j-7] + s1) & MASK32)

        # Ініціалізуємо робочі змінні поточними значеннями хешу
        a, b, c, d, e, f, g, hh = h

        # 64 раунди стиснення
        for j in range(64):
            # Σ1 — функція суми для e
            S1 = ror32(e, 6) ^ ror32(e, 11) ^ ror32(e, 25)
            # Ch (вибір) — якщо біт e=1, беремо f; якщо 0 — g
            ch = (e & f) ^ (~e & g) & MASK32
            temp1 = (hh + S1 + ch + K[j] + w[j]) & MASK32

            # Σ0 — функція суми для a
            S0 = ror32(a, 2) ^ ror32(a, 13) ^ ror32(a, 22)
            # Maj (більшість) — біт 1 якщо хоча б два з {a,b,c} = 1
            maj = (a & b) ^ (a & c) ^ (b & c)
            temp2 = (S0 + maj) & MASK32

            # Зсув робочих змінних
            hh = g
            g  = f
            f  = e
            e  = (d + temp1) & MASK32
            d  = c
            c  = b
            b  = a
            a  = (temp1 + temp2) & MASK32

        # Додаємо результат раунду до поточного хешу
        h[0] = (h[0] + a)  & MASK32
        h[1] = (h[1] + b)  & MASK32
        h[2] = (h[2] + c)  & MASK32
        h[3] = (h[3] + d)  & MASK32
        h[4] = (h[4] + e)  & MASK32
        h[5] = (h[5] + f)  & MASK32
        h[6] = (h[6] + g)  & MASK32
        h[7] = (h[7] + hh) & MASK32

    # Збираємо фінальний хеш (8 слів × 4 байти = 32 байти = 256 біт)
    return ''.join(f'{x:08x}' for x in h)


# ──────────────────────────────────────────────────────────
# Хешування пароля з сіллю (Salt)
#
# Сіль — випадковий рядок, що додається до пароля перед хешуванням.
# Захищає від атак за словником та rainbow-таблицями:
# навіть однакові паролі дають різні хеші.
# ──────────────────────────────────────────────────────────

import random
import string

def generate_salt(length=16) -> str:
    """Генерує випадкову сіль із букв та цифр."""
    chars = string.ascii_letters + string.digits
    return ''.join(random.choice(chars) for _ in range(length))

def hash_password(password: str, salt: str = None):
    """
    Хешує пароль з сіллю.
    Якщо сіль не передана — генерується нова.
    Повертає (salt, hash).
    """
    if salt is None:
        salt = generate_salt()
    salted = salt + password       # сіль + пароль
    hashed = sha256(salted)
    return salt, hashed

def verify_password(password: str, salt: str, stored_hash: str) -> bool:
    """
    Перевіряє пароль: хешує введений пароль з тією ж сіллю
    та порівнює з збереженим хешем.
    """
    _, computed = hash_password(password, salt)
    return computed == stored_hash


