# K — перші 32 біти дробових частин кубічних коренів
#     перших 64 простих чисел
# H — початкові значення хешу (перші 32 біти дробових частин
#     квадратних коренів перших 8 простих чисел)
# ──────────────────────────────────────────────────────────

