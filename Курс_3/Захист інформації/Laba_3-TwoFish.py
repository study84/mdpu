MASK32 = 0xFFFFFFFF
RHO    = 0x01010101


# ──────────────────────────────────────────────────────────
# Допоміжні функції
# ──────────────────────────────────────────────────────────

def ror32(x, n):
    return ((x >> n) | (x << (32 - n))) & MASK32

def rol32(x, n):
    return ((x << n) | (x >> (32 - n))) & MASK32

def gf_mul(a, b):
    """Множення у GF(2^8) з поліномом x^8+x^6+x^3+x^2+1 (0x169)."""
    r = 0
    for _ in range(8):
        if b & 1:
            r ^= a
        if a & 0x80:
            a = ((a << 1) ^ 0x69) & 0xFF
        else:
            a = (a << 1) & 0xFF
        b >>= 1
    return r

def mds_multiply(v):
    """Множення вектора [b0,b1,b2,b3] на MDS-матрицю."""
    return [
        gf_mul(MDS[i][0], v[0]) ^ gf_mul(MDS[i][1], v[1]) ^
        gf_mul(MDS[i][2], v[2]) ^ gf_mul(MDS[i][3], v[3])
        for i in range(4)
    ]

def pht(a, b):
    """Pseudo-Hadamard Transform."""
    return (a + b) & MASK32, (a + 2 * b) & MASK32


# ──────────────────────────────────────────────────────────
# Функція h
# ──────────────────────────────────────────────────────────

def h(x, key_words):
    """Підстановка 32-бітного слова залежна від ключа."""
    b = list(struct.pack('<I', x))  # 4 байти little-endian

    kw0 = list(struct.pack('<I', key_words[0]))
    kw1 = list(struct.pack('<I', key_words[1]))

    b = [
        Q1[Q0[Q0[b[0]] ^ kw1[0]] ^ kw0[0]],
        Q0[Q0[Q1[b[1]] ^ kw1[1]] ^ kw0[1]],
        Q1[Q1[Q0[b[2]] ^ kw1[2]] ^ kw0[2]],
        Q0[Q1[Q1[b[3]] ^ kw1[3]] ^ kw0[3]],
    ]

    z = mds_multiply(b)
    return struct.unpack('<I', bytes(z))[0]


# ──────────────────────────────────────────────────────────
# Розширення ключа
# ──────────────────────────────────────────────────────────

def key_schedule(key: bytes):
    """Генерація 40 підключів з 16-байтного ключа."""
    key = key[:16].ljust(16, b'\x00')
    words = list(struct.unpack('<4I', key))

    Me = [words[0], words[2]]   # парні слова
    Mo = [words[1], words[3]]   # непарні слова

    subkeys = []
    for i in range(20):
        A = h((RHO * (2 * i)) & MASK32, Me)
        B = h((RHO * (2 * i + 1)) & MASK32, Mo)
        B = rol32(B, 8)
        K0, K1 = pht(A, B)
        K1 = rol32(K1, 9)
        subkeys.extend([K0, K1])

    return subkeys, Me


# ──────────────────────────────────────────────────────────
# Шифрування / дешифрування блоку
# ──────────────────────────────────────────────────────────

def encrypt_block(block: bytes, subkeys: list, s_list: list) -> bytes:
    """Шифрування одного блоку 16 байт."""
    A, B, C, D = struct.unpack('<4I', block)

    # Input whitening
    A ^= subkeys[0]; B ^= subkeys[1]
    C ^= subkeys[2]; D ^= subkeys[3]

    for r in range(16):
        T0 = h(A, s_list)
        T1 = h(rol32(B, 8), s_list)
        T0, T1 = pht(T0, T1)

        C = ror32(C ^ (T0 + subkeys[2*r + 8]) & MASK32, 1)
        D = (rol32(D, 1) ^ (T1 + subkeys[2*r + 9])) & MASK32

        A, B, C, D = C, D, A, B  # своп

    # Output whitening (з урахуванням останнього свопу)
    return struct.pack('<4I',
        C ^ subkeys[4], D ^ subkeys[5],
        A ^ subkeys[6], B ^ subkeys[7])


def decrypt_block(block: bytes, subkeys: list, s_list: list) -> bytes:
    """Дешифрування одного блоку 16 байт."""
    C, D, A, B = struct.unpack('<4I', block)

    # Обернений output whitening
    C ^= subkeys[4]; D ^= subkeys[5]
    A ^= subkeys[6]; B ^= subkeys[7]

    for r in range(15, -1, -1):
        A, B, C, D = C, D, A, B  # обернений своп

        T0 = h(A, s_list)
        T1 = h(rol32(B, 8), s_list)
        T0, T1 = pht(T0, T1)

        C = (rol32(C, 1) ^ (T0 + subkeys[2*r + 8])) & MASK32
        D = ror32(D ^ (T1 + subkeys[2*r + 9]) & MASK32, 1)

    # Обернений input whitening
    return struct.pack('<4I',
        A ^ subkeys[0], B ^ subkeys[1],
        C ^ subkeys[2], D ^ subkeys[3])


# ──────────────────────────────────────────────────────────
# Padding та підготовка ключа
# ──────────────────────────────────────────────────────────

def pad(data: bytes) -> bytes:
    """PKCS#7 padding до кратного 16 байт."""
    n = 16 - len(data) % 16
    return data + bytes([n] * n)

def unpad(data: bytes) -> bytes:
    """Видалення PKCS#7 padding."""
    return data[:-data[-1]]

def make_key(keyword: str) -> bytes:
    """Отримує 16-байтний ключ зі слова-ключа."""
    raw = keyword.encode('utf-8')
    if len(raw) < 16:
        raw = (raw * (16 // len(raw) + 1))[:16]
    return raw[:16]


# ──────────────────────────────────────────────────────────
# Публічний інтерфейс
# ──────────────────────────────────────────────────────────

def twofish_encrypt(plaintext: str, keyword: str) -> bytes:
    """Зашифрувати рядок, повернути байти."""
    subkeys, s_list = key_schedule(make_key(keyword))
    data = pad(plaintext.encode('utf-8'))
    return b''.join(encrypt_block(data[i:i+16], subkeys, s_list)
                    for i in range(0, len(data), 16))

def twofish_decrypt(ciphertext: bytes, keyword: str) -> str:
    """Розшифрувати байти, повернути рядок."""
    subkeys, s_list = key_schedule(make_key(keyword))
    result = b''.join(decrypt_block(ciphertext[i:i+16], subkeys, s_list)
                      for i in range(0, len(ciphertext), 16))
    return unpad(result).decode('utf-8')


